r= range(5)
print(r)        #Output : range(0,5)
print(list(r))  #Output : [0,1,2,3,4]


#explaination: The `range()` function generates a sequence of numbers starting from 0 (by default) up to, but not including, the specified number (in this case, 5). When you print `r`, it shows the range object itself, which is `range(0, 5)`. When you convert the range object to a list using `list(r)`, it produces a list of numbers from 0 to 4, which is `[0, 1, 2, 3, 4]`.