#Predict the output:

b= bytes([65, 66, 67])
print(b)

#Output : b'ABC'
#Explanation: The `bytes()` function creates a bytes object from the given list of integers. Each integer in the list represents an ASCII value. In this case, 65 corresponds to 'A', 66 corresponds to 'B', and 67 corresponds to 'C'. Therefore, when you print the bytes object `b`, it displays `b'ABC'`, indicating that the bytes object contains the ASCII characters 'A', 'B', and 'C'.