# Q7 : why does the input() function always return a string
# How can you convert it to another data type?

# The input() function in Python always returns a string because it is designed to read user input as text. This allows users to enter any type of data, including numbers, without causing errors. 
# To convert the input to another data type, 
# you can use type conversion functions such as int(), float(), or bool() depending on the desired data type.
#For example, if you want to convert the input to an integer, you can use int(input()).