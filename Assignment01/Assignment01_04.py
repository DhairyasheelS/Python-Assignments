# Q4 : what is purpose of getsizeof()?
# why is memory size different for different data types?

# The getsizeof() function in Python is used to get the size of an object in bytes. 
# It returns the memory size of the object, 
# including the overhead of the object itself and 
# any additional memory used by the object to store its data.

# The memory size is different for different data types because different data types have different memory requirements. 
# For example, an integer typically requires less memory than a string or a list because it is a simpler data type.
# Additionally, the memory size of an object can also depend on the implementation of Python and the underlying hardware architecture.