# Q6 : Write a program that accepts a two numbers from user and prints their :
# addition
# subtraction
# multiplication
# division

print("Enter first number : ")
iNo1 = int(input())

print("Enter second number : ")
iNo2 = int(input())

iAns = iNo1 + iNo2
iSub = iNo1 - iNo2
iMul = iNo1 * iNo2
iDiv = iNo1 / iNo2

print("Addition of two numbers is:",iAns)
print("Substraction of two numbers are :" , iSub)
print("Multiplication of two numbers are :", iMul)
print("Division of two numbers are :", iDiv)
